package Models;

/**
 *
 * @author Admin
 */
public class CarBrand {
    public int id;
    public String name;

    public CarBrand() {
    }

    public CarBrand(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Car_Brand{" + "id=" + id + ", name=" + name + '}';
    }
    
    
}
